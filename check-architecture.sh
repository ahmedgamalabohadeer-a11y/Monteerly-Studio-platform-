#!/bin/bash
echo "====================================================="
echo "🔍 تقرير التدقيق المعماري: خريطة صفحات Monteerly OS"
echo "====================================================="

echo -e "\n📍 1. حالة الصفحة الرئيسية (بوابة العبور):"
if [ -f "src/app/page.tsx" ]; then
    echo "✅ [موجودة] وتعمل كمسار أساسي في: src/app/page.tsx"
elif [ -f "app/page.tsx" ]; then
    echo "✅ [موجودة] وتعمل كمسار أساسي في: app/page.tsx"
else
    echo "❌ [مفقودة] النظام يفتقر إلى صفحة توجيه رئيسية (404 محتمل)!"
fi

echo -e "\n📍 2. الجرد الشامل لجميع واجهات المشروع (Pages & Layouts):"
# البحث عن جميع ملفات الصفحات والهياكل مع استبعاد مجلدات النظام المؤقتة
find . \( -name "page.tsx" -o -name "layout.tsx" \) -not -path "*/node_modules/*" -not -path "*/.next/*" | sort | sed 's|^\./||' | while read line; do
    echo " 📄 $line"
done

echo "====================================================="
echo "✅ اكتمل المسح الاستراتيجي بنجاح."
